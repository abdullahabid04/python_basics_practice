while 1:
    num1 = int(input("Enter a number : "))
    num2 = int(input("Enter a number : "))
    choice = int(input("Enter your choice : "))
    if choice == 1:
        print(num1 + num2)
    if choice == 2:
        print(num1 - num2)
    if choice == 3:
        print(num1 * num2)
    if choice == 4:
        print(num1 / num2)
